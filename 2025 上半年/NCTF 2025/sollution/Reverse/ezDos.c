int encrypt(_BYTE flag[], int len)
{
    int i = 0, j = 0;
    for (int ii = 0; ii < len; ii++)
    {
        i = (i + 1) % 256;
        j = (j + S_Box[i]) % 256;

        int temp = S_Box[j];
        S_Box[j] = S_Box[i];
        S_Box[i] = temp;

        flag[ii] ^= S_Box[(S_Box[i] + S_Box[j]) % 256] + 2;
        j = (j + 0) % 256;
    }
}

int decrypt(_BYTE flag[], int len)
{
    unsigned int i = 0, j = 0;
    for (int ii = 0; ii < len; ii++)
    {
        i = (i + 1) % 256;
        j = (j + S_Box[i]) % 256;

        int temp = S_Box[j];
        S_Box[j] = S_Box[i];
        S_Box[i] = temp;
    }

    for (int ii = len - 1; ii >= 0; ii--)
    {
        flag[ii] ^= S_Box[(S_Box[i] + S_Box[j]) % 256] + 1;

        int temp = S_Box[j];
        S_Box[j] = S_Box[i];
        S_Box[i] = temp;

        j = (j - S_Box[i]) % 256;
        i = (i - 1) % 256;
    }
}

